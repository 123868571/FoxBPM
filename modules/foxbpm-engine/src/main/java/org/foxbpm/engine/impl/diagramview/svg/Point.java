package org.foxbpm.engine.impl.diagramview.svg;

/**
 * 坐标辅助类
 * 
 * @author MAENLIANG
 * @date 2014-06-13
 * 
 */
public final class Point {
	private float x = 0.0f;
	private float y = 0.0f;

	public Point(float x, float y) {
		this.x = x;
		this.y = y;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
}
